// Conditional Statements
let a=10;
let b=50;
if(b>a){
    console.log("b is greater");
}
else{
    console.log("a is greater");
}

//for statement
for(let i=1;i<10;i++)
{
    console.log(i);
}

//while statement
let j=1;
while(j<5)
{
    console.log(j);
    j++;
}

//do while
let k=1;
do{
    console.log(k);
}
while(k<8);
